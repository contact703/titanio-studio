// Função para obter versículo do dia
export function getDailyVerse(date?: Date): DailyVerse {
  const today = date || new Date();
  const startOfYear = new Date(today.getFullYear(), 0, 0);
  const diff = today.getTime() - startOfYear.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);
  
  // Garantir que está no range 1-365
  const day = ((dayOfYear - 1) % 365) + 1;
  
  return DAILY_VERSES.find(v => v.day === day) || DAILY_VERSES[0];
}

// Função para obter versículo aleatório por tema
export function getVerseByTheme(theme: string): DailyVerse | null {
  const verses = DAILY_VERSES.filter(v => 
    v.theme.toLowerCase() === theme.toLowerCase()
  );
  
  if (verses.length === 0) return null;
  
  return verses[Math.floor(Math.random() * verses.length)];
}

